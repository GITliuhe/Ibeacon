package com.liu.bsbeacon.vo;

public class CoordinateFormatVO {

    /**
     * 映射X坐标到前端页面 left属性
     *
     */
    private int left;

    /**
     * 映射Y坐标到前端 top 属性
     *
     */
    private int top;


    public int getLeft() {
        return left;
    }

    public void setLeft(int left) {
        this.left = left;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }
}
